public class Student {
    
    private String roll;
    private String name;

    public String getName()
    {

        return name;

    }

    public String getRoll()
    {

        return roll;

    }

    public void setName(String name)
    {

        this.name=name;

    }

    public void setRoll(String roll)
    {

        this.roll=roll;

    }

}
